public class temp_change  
{
    public static void main(String[] args)
    {
        float c2=20;
        float f1=25;
        float f2=c2*(9/5)+32;
        float c1=(f1-32)*5/9;
        System.out.println("\n -------C to F -----\n Default temp in C "+c2+"\n Converted temp in F "+f2);
        System.out.println("\n\n ------ F to C ------\n Default temp in F "+f1+"\n Converted temp in C "+c1+"\n");
    }
}
